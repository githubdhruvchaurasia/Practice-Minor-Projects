import os 

def create_file(filename):
    try:
        with open(filename, 'x') as f:
            print(f"File Name {filename} created successfully!")

    except FileExistsError:
        print(f"File name {filename} already exists!")

    except Exception as E:
        print("An Error Occured!")


def view_all_files():
    files = os.listdir()
    if not files:
        print("No file found!")
    else:
        print("Files are in directory!")
        for file in files:
            print(file)


def delete_file (filename):
    try:
        os.remove(filename)
        print(f'{filename} has been deleted successfully')
    except FileNotFoundError:
        print("File not found!")

    except Exception as e:
        print("An error occured!")


def read_file(filename):
    try:
        with open("sample.txt", "r") as f:
            content = f.read()
            print(f"Content of '{filename}' :\n{content} ")

    except FileNotFoundError:
        print(f"{filename} doesn't exist!")

    except Exception as e:
        print('An error occured!')


def edit_file(filename):
    try:
        with open('sample.txt', 'a') as f:
            content = input('Enter data to add : ')
            f.write(content + "\n")
            print(f"Content has been updated successfully!")

    except FileNotFoundError:
        print(f"{filename} doesn't exist!")

    except Exception as e:
        print('An error occured!') 


def main():
    while True:
        print(f"FILE MANAGEMENT APP")
        print('1 : Create File')
        print('2 : View all Files')
        print('3 : Delete File')
        print('4 : Read File')
        print('5 : Edit File')
        print('6 : Exit')
        
        choice = input("Enter Your choice between 1 To 6 : ")

        if choice == '1':
            filename = input ("Enter the file name you want to create : ")
            create_file(filename)

        elif choice == '2':
            view_all_files()

        elif choice == '3':
            filename = input("Enter the file name you want to delete : ")
            delete_file(filename)

        elif choice == '4':
            filename = input("Enter the file name which you want to read : ")
            read_file(filename)

        elif choice == '5':
            filename = input("Enter the file name you want to edit : ")
            edit_file(filename)

        elif choice == '6':
            print('Closing the app.....')
            break

        else:
            print("Invalid Syntax")

if __name__ == "__main__":
    main()